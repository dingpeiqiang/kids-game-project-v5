-- ================================================
-- 游戏状态枚举优化 - 数据库迁移说明
-- ================================================
-- 说明：本次优化不需要修改数据库表结构
--      游戏状态字段仍然使用 INTEGER 类型存储
--      变更主要在应用层使用枚举类管理状态
-- ================================================

-- 现有 t_game 表的状态字段定义：
-- status INT COMMENT '状态：0-草稿，1-待审核，2-已上架，3-已下架，4-审核驳回'

-- 状态枚举对应关系：
-- 0 - DRAFT (草稿)
-- 1 - PENDING_REVIEW (待审核)
-- 2 - ON_SALE (已上架)
-- 3 - OFFLINE (已下架)
-- 4 - REJECTED (审核驳回)

-- ================================================
-- 如果需要更新现有游戏数据的状态，可以使用以下 SQL：
-- ================================================

-- 示例 1: 将所有禁用的游戏标记为已下架
-- UPDATE t_game SET status = 3 WHERE status = 0;

-- 示例 2: 将所有启用的游戏标记为已上架
-- UPDATE t_game SET status = 2 WHERE status = 1;

-- ================================================
-- 验证当前游戏状态分布
-- ================================================
SELECT 
    CASE status
        WHEN 0 THEN '草稿'
        WHEN 1 THEN '待审核'
        WHEN 2 THEN '已上架'
        WHEN 3 THEN '已下架'
        WHEN 4 THEN '审核驳回'
        ELSE '未知'
    END AS status_name,
    COUNT(*) AS game_count
FROM t_game
WHERE deleted = 0
GROUP BY status
ORDER BY status;

