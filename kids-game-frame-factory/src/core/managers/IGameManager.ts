/**
 * ⭐ 游戏管理器接口
 * 
 * @remarks
 * 所有管理器必须实现此接口，以确保:
 * - 统一的初始化流程
 * - 统一的更新机制
 * - 统一的销毁清理
 */
export interface IGameManager {
  /**
   * ⭐ 初始化
   * 
   * @remarks
   * 在场景 create() 阶段调用，进行一次性初始化
   */
  init(): void
  
  /**
   * ⭐ 每帧更新（可选）
   * 
   * @param time - 游戏运行总毫秒数
   * @param delta - 距离上一帧的毫秒数
   * 
   * @remarks
   * 不是所有管理器都需要 update 方法
   * 例如 EntityManager 通常不需要每帧更新
   */
  update?(time: number, delta: number): void
  
  /**
   * ⭐ 销毁清理
   * 
   * @remarks
   * 在场景 shutdown() 阶段调用，清理所有资源
   */
  destroy(): void
}
